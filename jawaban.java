public class TipeDataJava {
    public static void main(String[] args) {
        // Tipe data byte
        byte contohByte = 10;
        System.out.println("Tipe data byte: " + contohByte);

        // Tipe data short
        short contohShort = 100;
        System.out.println("Tipe data short: " + contohShort);

        // Tipe data int
        int contohInt = 1000;
        System.out.println("Tipe data int: " + contohInt);

        // Tipe data long
        long contohLong = 100000L;
        System.out.println("Tipe data long: " + contohLong);

        // Tipe data float
        float contohFloat = 12.34f;
        System.out.println("Tipe data float: " + contohFloat);

        // Tipe data double
        double contohDouble = 123.456;
        System.out.println("Tipe data double: " + contohDouble);

        // Tipe data char
        char contohChar = 'A';
        System.out.println("Tipe data char: " + contohChar);

        // Tipe data boolean
        boolean contohBoolean = true;
        System.out.println("Tipe data boolean: " + contohBoolean);
    }
}
